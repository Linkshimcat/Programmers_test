class Solution {
    public int solution(int num1, int num2) {
        int answer = -1;
        if (num1 < num2) {
            int result = -1;
            return result;
        }
        else if (num1 == num2) {
            int result2 = 1;
            return result2;
        }
        else {
            return answer;
        }
    }
}